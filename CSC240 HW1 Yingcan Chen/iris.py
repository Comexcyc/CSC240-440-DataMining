fig = plt.figure()
ax1 = fig.add_subplot(111)
ax1.scatter(x=data[['sep_length']].loc[data['class']=='Iris-setosa'],y=data[['sep_width']].loc[data['class']=='Iris-setosa'],s=10,c='b',marker="s")
ax1.scatter(x=data[['sep_length']].loc[data['class']=='Iris-virginica'],y=data[['sep_width']].loc[data['class']=='Iris-virginica'],s=10,c='r',marker="s")
