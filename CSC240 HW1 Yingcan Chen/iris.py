import pandas as pd
import matplotlib.pyplot as plt
data = pd.read_csv('iris.txt', sep=",", header=None)
data.columns=["sep_length", "sep_width", "pet_length", "pet_width", "class"]

print (data)
#IRIS_SETOSA
print("Iris_setosa Mean")
print(data[['sep_length','sep_width','pet_length','pet_width']].loc[data['class']=='Iris-setosa'].mean())
print("Iris_setosa Max")
print(data[['sep_length','sep_width','pet_length','pet_width']].loc[data['class']=='Iris-setosa'].max())
print("Iris_setosa Min")
print(data[['sep_length','sep_width','pet_length','pet_width']].loc[data['class']=='Iris-setosa'].min())

